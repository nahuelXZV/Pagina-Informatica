                <p class="mb-4 text-xl font-bold text-gray-800 uppercase">
                    Historia
                </p>
                <p>
                    La carrera de Ingeniería Informática perteneciente a la Facultad de Ciencias Exactas y Tecnología de
                    la Universidad Autónoma “Gabriel René Moreno”, es una carrera que forma profesionales en el área de
                    Ciencias de la Computación. En sus inicios parte con una población estudiantil de 38 alumnos que
                    luego se incrementa notablemente, hasta pasar a ser la carrera con mayor demanda dentro de la
                    facultad y la universidad. Actualmente se cuanta con aproximadamente 2600 y con una tendencia a
                    seguir creciendo.
                </p>
                <br>
                <p>
                    Como inicio oficial de la carrera de Ingeniería Informática se establece el año 1987, pero el primer
                    intento para su creación se realizo el año 1979, con el golpe de estado militar de 1980 y la
                    intervención de la universidad este proceso se corto y con la vuelta a la democracia en 1985 se
                    retomo la idea de la creación al impulso de lo que en ese momento era el departamento de
                    matemáticas, que luego se consolido en 1987.
                </p>
                <br>
                <p>
                    En el mes de Junio de 1987 se crea la Carrera de Ingeniería Informática, de acuerdo a Resoluciones
                    Nro. 3/87, 4/87, 5/87 del Consejo Facultativo de la Facultad de Ciencias Exactas y Tecnología.
                </p>
                <br>
                <p>
                    El 16 de Julio de 1987, mediante resolución I.C.U. 32/87 el Ilustre Consejo Universitario convalida
                    las resoluciones facultativas y crea la Carrera de Ingeniería Informática a nivel licenciatura bajo
                    el régimen anualizado, para satisfacer la demanda generada por el desarrollo técnico-científico de
                    la informática a nivel mundial.
                </p>
                <br>
                <p>
                    En esa misma fecha la carrera comienza sus actividades, teniendo como su primer jefe de departamento
                    al Ing. Mario Señoranis, dándole la orientación de Informática de gestión, con proyección al
                    desarrollo de Software de aplicación y en particular de Sistemas de Información.
                </p>
                <br>
                <p>
                    En 1990 bajo la dirección del Ingeniero Orlando Veramendi, se pasa del plan 187-0 (Plan de estudio
                    Inicial) al plan 187-1, donde se eliminan tres materias y se cambian de nombre a dos de las
                    materias, manteniéndose el régimen anual.
                </p>
                <br>
                <p>
                    En 1992 también con el Ingeniero Orlando Veramendi se cambia la modalidad de egreso, eliminando los
                    exámenes orales con tribunal que permitían un egreso directo y sustituyéndolo por un proyecto de
                    grado o tesis.
                    En 1995-1996 bajo la dirección del Ingeniero Mario Zeballos, después de las 3ra Jornadas Académicas
                    se procede a cambiar los pre-requisitos de algunas las materias, manteniendo el plan 187-1 y el
                    régimen anual.
                </p>
                <br>
                <p>
                    En el año 2003 con la dirección del Ingeniero Miguel Peinado, se pone en vigencia el Plan 182-2 con
                    la creación de una nueva malla curricular en la cual se establecen dos menciones (Ingeniería en
                    Informática e Ingeniería en Sistemas) y se cambia de un sistema anualizado a uno semestralizado.
                </p>
                <br>
                <p>
                    Para la elaboración del primer plan de estudios de la carrera, se tomó como base el estudio
                    realizado por la “Technical Committee for Education” de la International Federation For Information
                    Processing por encargo de la Unesco, estudio publicado con el titulo de “A Modular Curriculum in
                    Computer Science” que fue revisado por la World Conference on Computer Education, realizado en Suiza
                    en Julio de 1981.
                </p>
                <br>
                <p>
                    El diseño de la nuevo diseño curricular correspondiente al Plan 187-2, se basa en el estudio
                    realizado por la IEEE Computer Society y ACM Association for Computing Machinery: “The Joint Task
                    Force on Computing Curricula”, con el titulo de “Computing Curricula 2001 Computer Science”
                    publicado el 15 de diciembre del 2001.
                </p>

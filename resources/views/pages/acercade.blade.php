            <div class="md:pr-7 text-justify">
                <p class="mb-4 text-xl font-bold text-gray-800 uppercase">
                    Historia
                </p>
                <p>
                    La carrera de Ingeniería Informática perteneciente a la Facultad de Ciencias De la Computación y
                    Telecomunicaciones de la Universidad Autónoma “Gabriel René Moreno”, es una carrera que forma
                    profesionales en el área de Ciencias de la Computación.
                </p>
                <br>
                <p>
                    Se inició con una población estudiantil de 38 alumnos que luego se incrementa notablemente, hasta
                    pasar a ser la carrera con mayor demanda dentro de la universidad.
                </p>
                <br>
                <p>
                    El primer intento para su creación se realizó el año 1979, pero, el golpe de estado militar de 1980
                    y la intervención de la universidad impidieron este proceso. Con la vuelta a la democracia en el
                    país, en 1985 se retomó la idea de la creación, al impulso de lo que en ese momento era el
                    departamento de matemáticas que realizo un estudio de mercado y por lo resultados obtenidos se
                    decide la creación de la carrera de ingeniería informática.
                </p>
                <br>
                <p>
                    En el mes de Junio de 1987 oficialmente se crea la Carrera de Ingeniería Informática, de acuerdo a
                    Resoluciones Nro. 3/87, 4/87, 5/87 del Consejo Facultativo de la Facultad de Ciencias Exactas y
                    Tecnología. El 16 de Julio de 1987, mediante resolución I.C.U. 32/87 el Ilustre Consejo
                    Universitario convalida las resoluciones facultativas y crea la Carrera de Ingeniería Informática a
                    nivel licenciatura bajo el régimen anualizado, siguiendo el nuevo modelo académico, para satisfacer
                    la demanda generada por el desarrollo técnico científico de la informática a nivel mundial.
                </p>
                <br>
                <p>
                    En septiembre de 1987 bajo la dirección del Ing. Mario Señoranis jefe del departamento de
                    matemáticas la carrera inicio sus actividades con el Plan 187-0, con proyección al desarrollo de
                    Software de aplicación y en particular de Sistemas de Información. Tenía las siguientes
                    características: Régimen anualizado, 28 materias, 2 parciales y un examen final daban al alumno el
                    derecho al examen oral con tribunal, aprobado todas las materias el alumno recibía su título
                    académico y provisión nacional sin tesis y otra modalidad de graduación.
                </p>
                <br>
                <p>
                    En 1988 el Ing. Erick Belmonte Clementely fue nombrado jefe de la carrera de Ingeniería Informática.
                </p>
                <br>
                <p>
                    En 1990 bajo la dirección del Ing. Orlando Veramendi, Se realizaron las primeras jornadas académicas
                    de la carrera, obteniendo como resultado el cambio del plan 187-0 (plan de estudio inicial) al plan
                    187-1, continuando el plan anualizado y dejando la evaluación final oral con tribunal.
                </p>
                <br>
                <p>
                    En 1992 se cambió la modalidad de egreso, eliminando los exámenes orales con tribunal que permitían
                    un egreso directo, sustituyéndolo por un proyecto de grado o tesis.
                </p>
                <br>
                <p>
                    En 1995-1996 bajo la dirección del Ingeniero Mario Zeballos, después de las 3ras Jornadas Académicas
                    se procedió a cambiar los pre-requisitos de algunas materias, manteniendo el plan 187-1 y el régimen
                    anualizado.
                </p>
                <br>
                <p>
                    En el año 2003 con la dirección del Ingeniero Miguel Peinado, se puso en vigencia el Plan 187-2 con
                    la creación de una nueva malla curricular en la cual se establecen dos menciones: Ingeniería en
                    Informática e Ingeniería en Sistemas y se cambia de un plan anualizado a semestral.
                </p>
                <br>
                <p>
                    Para la elaboración del primer plan de estudios de la carrera, se tomó como base el estudio
                    realizado por la “Technical Committee for Education” de la Internacional Federation For Information
                    Processing por encargo de la Unesco, estudio publicado con el titulo de “A Modular Curriculum in
                    Computer Science” que fue revisado por la World Conference on Computer Education, realizado en Suiza
                    en Julio de 1981.
                </p>
                <br>
                <p>
                    El nuevo diseño curricular correspondiente al Plan 187-2, se basa en el estudio realizado por la
                    IEEE Computer Society y ACM Association for Computing Machinery: “The Joint Task Force on Computing
                    Curricula”, con el titulo de “Computing Curricula 2001 Computer Science” publicado el 15 de
                    diciembre del 2001
                </p>
                <br>
                <p>
                    El año 2005 bajo la dirección del Ing. Juan Carlos Contreras un equipo de especialistas elaboran el
                    actual diseño curricular plan 187-3 bajo el régimen semestral. Se crea una nueva malla curricular,
                    con dos menciones (Ingeniería en Redes y Telecomunicaciones e Ingeniería en Sistemas), en fecha
                    20/05/2010 el ICU reconoce como fecha de creación de estas dos carreras el 20/04/2005.
                </p>
                <br>
                <p>
                    El año 2008 en la etapa del Ing. Juan Carlos Contreras se envía al abog. Reymi Ferreira como
                    presidente del ICU, el proyecto de creación de la Facultad de Ingeniería en Ciencias de la
                    Computación y Telecomunicaciones aprobado mediante resolución de carrera 5/2008
                </p>
                <br>
                <p>
                    El año 2011 bajo la dirección del Ing. Juan Carlos Contreras se acredita ante el CEUB la carrera de
                    Ingeniería Informática.
                </p>
                <br>
                <p>
                    En la etapa del Ing. Juan Carlos Contreras, se formó una comisión de docentes y estudiantes de la
                    carrera de Ingeniería Informática, quienes elaboraron el proyecto de creación de la facultad, que
                    fue sometido al análisis del Ilustre Consejo Universitario en su comisión académica. Esta, en el
                    primer informe solicitó el plan de viabilidad que posteriormente fue aprobado dando lugar a la
                    creación de la Facultad de Ingeniería en Ciencias de la Computación y Telecomunicaciones el 6 de
                    Marzo del 2012 mediante resolución ICU 18/2012.
                </p>
                <br>
                <p>
                    El año 2017 Bajo la dirección del Ing. Franklin Calderón Flores se realiza la sectorial de la
                    carrera de Ingeniería Informática en instalaciones de la UAGRM, convocada por el CEUB.
                </p>
                <br>
                <p>
                    En el año 2023, Bajo la dirección de la MSc. Shirley Pérez Delgadillo, se inició con el proceso de
                    rediseño por competencias. Con la conformación de las Comisiones según la Resolución Consejo
                    Facultativo Nro. 51/2022.
                </p>
                <br>
                <p>
                    El 08 de mayo del 2023, se dio inicio al 4to proceso de Autoevaluación con la conformación de las
                    comisiones Resolución del Consejo de Carrera Nro. 04/2023.
                </p>
            </div>
